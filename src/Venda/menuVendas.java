    public void addLinhaTabela() throws ClassNotFoundException, SQLException{
        DefaultTableModel model = (DefaultTableModel) tbMeds.getModel();
        ArrayList<Medicamento> lstMeds = dbMed.listar("");
        Object infoLinha [] = new Object[4];
        for (int i=0; i<lstMeds.size(); i++){
            infoLinha[0] = lstMeds.get(i).getIdMed();
            infoLinha[1] = lstMeds.get(i).getNome();
            infoLinha[2] = lstMeds.get(i).getTarja();
            infoLinha[3] = lstMeds.get(i).getPreco();
            model.addRow(infoLinha);
        }
    }
