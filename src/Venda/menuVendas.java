    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) tbCarrinho.getModel();
        // get selected row index
        try {
            int SelectedRowIndex = tbCarrinho.getSelectedRow();
            subtraiValorTotal();
            model.removeRow(SelectedRowIndex);
            preencheQtd();
        }
        catch(Exception ex){
            JOptionPane.showMessageDialog(null, "Selecione algum item para remover!");
            return;
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
            int aux = (int)tbMeds.getValueAt(tbMeds.getSelectedRow(), 0);
            somaValorTotal();
            addLinhaCarrinho(aux);
            preencheQtd();
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(menuVendas.class.getName()).log(Level.SEVERE, null, ex);
        } catch(ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(null, "Selecione um item para adicionar!");
            return;
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnConcedeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConcedeActionPerformed
        try {
            double valorAtual;
            double desconto;
            JOptionPane.showMessageDialog(null, "É necessário informar as credenciais do gerente para conceder o desconto!");
            String user = JOptionPane.showInputDialog(null, "Digite o nome de usuário: ");
            String pass = JOptionPane.showInputDialog(null, "Digite a senha: ");
            CaixaDAO dbCx = new CaixaDAO();
            if(dbCx.checkGerente(user,pass)){
                valorAtual = Double.valueOf(txtValorTotal.getText());
                //se for aposentado da desconto
                if(menuPrincipal.cli.isAposentado()){
                    desconto = Double.valueOf(txtDescAposentado.getText());
                    valorAtual -= desconto;
                    txtValorTotal.setText(String.valueOf(valorAtual));
                }
                else{
                    if (cmbTipoPag.getSelectedIndex()==1){                        
                        desconto = Double.valueOf(txtDescDinheiro.getText());
                        txtValorTotal.setText(String.valueOf(valorAtual));
                    }
                }
                JOptionPane.showMessageDialog(null,"Desconto Concedido com sucesso!");
            }                
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "Credencias invalidas");
            return;
        }
    }//GEN-LAST:event_btnConcedeActionPerformed

