    //somando valor total pra jogar no banco depois
    public void somaValorTotal(){
        double valorTotal;
        double vlComum;
        double vlAposentado;
        double valorNew = (double)tbMeds.getValueAt(tbMeds.getSelectedRow(),3);
        double valorAtual = Double.valueOf(txtValorTotal.getText());
        valorTotal = valorAtual + valorNew;
        DecimalFormat formato = new DecimalFormat("#.##", new DecimalFormatSymbols(Locale.US));      
        valorTotal = Double.valueOf(formato.format(valorTotal));
        txtValorTotal.setText(String.valueOf(valorTotal));
        if (menuPrincipal.cli.isAposentado()){
            vlAposentado = valorTotal * 0.2;
            vlAposentado = Double.valueOf(formato.format(vlAposentado));
            txtDescAposentado.setText(String.valueOf(vlAposentado));
        }
        else{
            vlComum = valorTotal * 0.05;
            vlComum = Double.valueOf(formato.format(vlComum));
            txtDescDinheiro.setText(String.valueOf(vlComum));
        }
    }
    
